	ORG	00H
	SJMP	PROG

	ORG	00BH		; interrupcao do timer/contador 0
	MOV	TH1, #07FH
	MOV	TL1, #00H
	MOV	TH0, #07FH
	CLR	TR0
	SETB	TR1
	RETI

	ORG	0001BH		; interrupcao do timer/contador1
	MOV	TH0, #07FH
	MOV	TL0, #00H
	MOV	TH1, #07FH
	CLR	TR1
	SETB	TR0
	RETI

PROG:	SETB	EA
	SETB	ET0
	SETB	ET1
	MOV	TMOD, #11H	; 11H = 00010001B
	MOV	TH0, #07FH
	MOV	TL0, #00H
	MOV	TH1, #07FH
	SETB	TR0

LOOP:	MOV	P1, TH0
	MOV	P2, TH1
	SJMP	LOOP
	END
